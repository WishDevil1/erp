public class GlobalLock
{
	private final String type;
	private final int id;
	
	public GlobalLock(String type, int id)
	{
		super();
		this.type = type;
		this.id = id;
	}

	public String getType()
	{
		return type;
	}

	public int getId()
	{
		return id;
	}

	@Override
	public int hashCode()
	{
		final int prime = 31;
		int result = 1;
		result = prime * result + id;
		result = prime * result + ((type == null) ? 0 : type.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj)
	{
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		GlobalLock other = (GlobalLock)obj;
		if (id != other.id)
			return false;
		if (type == null)
		{
			if (other.type != null)
				return false;
		}
		else if (!type.equals(other.type))
			return false;
		return true;
	}

	@Override
	public String toString()
	{
		return "GlobalLock [type=" + type + ", id=" + id + "]";
	}
}
